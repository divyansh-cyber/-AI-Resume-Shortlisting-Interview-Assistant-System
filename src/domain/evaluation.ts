import { ScoreCard } from './score';
import { VerificationResult } from './verification';

/**
 * Tier classification drives what happens next in the hiring pipeline.
 *
 *  A – Fast-track    → HR call + technical interview scheduled immediately
 *  B – Tech Screen   → async coding challenge or short technical screen
 *  C – Needs Review  → recruiter manual review before any action
 */
export type Tier = 'A' | 'B' | 'C';

export interface TierClassification {
  tier: Tier;
  /**
   * Human-readable rationale for the tier assignment.
   * This is the "explainability" output surfaced to recruiters.
   */
  rationale: string;
  /** The score thresholds that produced this tier */
  thresholds: TierThresholds;
}

/**
 * These thresholds are exported so they can be referenced in tests
 * and potentially made configurable per-org in the future.
 */
export interface TierThresholds {
  /** overall score ≥ this value → Tier A */
  tierA: number;
  /** overall score ≥ this value → Tier B (otherwise Tier C) */
  tierB: number;
}

export const DEFAULT_TIER_THRESHOLDS: TierThresholds = {
  tierA: 75,
  tierB: 50,
};

/**
 * Structured interview question generated by the LLM for a specific candidate.
 */
export interface InterviewQuestion {
  id: string;
  /** The dimension this question probes */
  dimension: 'technical' | 'behavioral' | 'ownership' | 'achievement' | 'gap';
  /**
   * "gap" questions target skills listed in the JD that the candidate is
   * missing — the most actionable category for the interviewer.
   */
  question: string;
  /**
   * Why this question was chosen for THIS candidate.
   * Links back to specific resume details so interviewers arrive prepared.
   */
  rationale: string;
  /** Suggested follow-up probes */
  followUps: string[];
  difficulty: 'easy' | 'medium' | 'hard';
}

/**
 * The complete evaluation result for one candidate ↔ job pair.
 * This is the top-level object persisted to the database and returned by the API.
 */
export interface EvaluationResult {
  id: string;
  candidateId: string;
  jobId: string;

  scoreCard: ScoreCard;
  tierClassification: TierClassification;
  verificationResult: VerificationResult | null;
  interviewQuestions: InterviewQuestion[];

  /**
   * Executive summary paragraph written by the LLM.
   * Targeted at a recruiter who may not read every detail.
   */
  executiveSummary: string;

  status: EvaluationStatus;
  createdAt: string;
  updatedAt: string;
}

export type EvaluationStatus =
  | 'pending'       // job accepted, not yet started
  | 'parsing'       // resume being parsed
  | 'scoring'       // scoring engine running
  | 'verifying'     // claim verification in progress
  | 'generating'    // interview questions being generated
  | 'completed'     // all steps done
  | 'failed';       // unrecoverable error
